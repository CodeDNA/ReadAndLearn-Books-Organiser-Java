    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       
        try {
            if(txtUsername.getText().equals("Enter Username") || String.valueOf(txtPassword.getPassword()).equals("Enter Password"))
            {
                JOptionPane.showMessageDialog(null, "Please complete the form","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            
            
            if(getUser()== null)
            {
                lblWarningMessage.setText("Incorrect Username-Password combination !");
                lblWarningMessage.setVisible(true);
                return;
            }
            
            User userDetail = new User(loggedInUser.getString("userId"),loggedInUser.getString("fName") ,loggedInUser.getString("lName") ,loggedInUser.getString("userEmail") ,loggedInUser.getString("userPassword"),loggedInUser.getInt("UserType"));
            User.currentUser = userDetail;
            
            Dashboard dashBoard = new Dashboard();
            dashBoard.pack();
            dashBoard.setLocationRelativeTo(null); //This will place the CreateAccount form at the center
            dashBoard.setVisible(true);
            this.dispose();
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

