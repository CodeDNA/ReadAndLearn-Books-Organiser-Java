    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        
        if(checkBlanks())
        {
            JOptionPane.showMessageDialog(null, "ERROR! : Please enter all fields!","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!passwordsMatch())
        {
            JOptionPane.showMessageDialog(null, "ERROR! : Passwords does not match!","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!isEmailFormatCorrect())
        {
            JOptionPane.showMessageDialog(null, "Please enter correct Email","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(txtFirstName.getText().isEmpty())
        {
            System.out.println("EMPTY NAME");
        }
        if(user.insertUser(txtFirstName.getText(), txtLastName.getText(), txtEmail.getText(), String.valueOf(txtPassword.getPassword()), userType))
         {
             JOptionPane.showMessageDialog(null, "Registration Successful. Click OK to go to Login.","Success", JOptionPane.INFORMATION_MESSAGE);
             login();
             
         }
        else
        {
           JOptionPane.showMessageDialog(null, "ERROR! : Email already in use. Please enter a different email address.","Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }
    }//GEN-LAST:event_btnNextActionPerformed

