   private boolean checkBlanks() {
        boolean isEmpty = false;
        
        if(txtFirstName.getText().isEmpty())
        {
            isEmpty = true;
            lblStarFirstName.setVisible(true);
        }
        
        if(txtLastName.getText().isEmpty())
        {
            isEmpty = true;
            lblStarLastName.setVisible(true);
        }
        
        if(txtEmail.getText().isEmpty())
        {
            isEmpty = true;
            lblStarEmail.setVisible(true);
        }
        
        if(String.valueOf(txtPassword.getPassword()).isEmpty())
        {
            isEmpty = true;
            lblStarPassword.setVisible(true);
        }
        
        if(String.valueOf(txtConfirmPasword.getPassword()).isEmpty())
        {
            isEmpty = true;
            lblStarConfirmPassword.setVisible(true);
        }
        return isEmpty;
    }

    private boolean passwordsMatch() {
        return (String.valueOf(txtPassword.getPassword()).equals(String.valueOf(txtConfirmPasword.getPassword())));        
    }

    private boolean isEmailFormatCorrect() {
        String emailRegex = "\\w+([-+.']\\w+)*@\\w+([-.]\\w+)*\\.\\w+([-.]\\w+)*";
        Pattern checkEmailPattern = Pattern.compile(emailRegex);
        Matcher emailMatcher = checkEmailPattern.matcher(txtEmail.getText());
        return (emailMatcher.matches());
    }
